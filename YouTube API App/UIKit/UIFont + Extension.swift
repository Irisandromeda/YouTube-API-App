import UIKit

extension UIFont {
    
    static func sfProDisplayBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "SFProDisplay-Bold", size: size)
    }
    
    static func sfProTextMedium(size: CGFloat) -> UIFont? {
        return UIFont(name: "SF-Pro-Display-Medium", size: size)
    }
    
    static func sfProTextRegular(size: CGFloat) -> UIFont? {
        return UIFont(name: "SF-Pro-Display-Regular", size: size)
    }
    
}
